    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        int countDrink = (int) spNumber.getValue();
        int rowCount = tbInfo.getRowCount();
        try {
            if (idban != 0) {
                //truong hop ban chua co CT_HoaDon
                if (rowCount == 0) {
                    HoaDonDB.insert(idban);
                    idHoaDon = HoaDonDB.maxID();
                    CTHoaDonDB.insert(idHoaDon, idMenu, countDrink);
                    tbListTaleMouseClicked(evt);
                } //truong hop ban da co CT_HoaDon
                else {
                    idHoaDon = (int) tbInfo.getModel().getValueAt(0, 1);

                    //truong hop mon da ton tai => update
                    if (MenuDB.checkDrink(idMenu, idHoaDon)) {
                        CTHoaDonDB.update(countDrink, idHoaDon, idMenu);
                        tbListTaleMouseClicked(evt);
                    } //truong hop mon chua ton tai => insert
                    else {
                        CTHoaDonDB.insert(idHoaDon, idMenu, countDrink);
                        tbListTaleMouseClicked(evt);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Chưa chọn bàn", "Thông báo", 1);
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_btnAddMouseClicked

    private void btnPayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPayMouseClicked
        if (tbInfo.getModel().getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn bàn hoặc bàn trống", "Thông báo", 2);
        } else if (0 == JOptionPane.showConfirmDialog(null, "Bạn có muốn thanh toán hóa đơn cho bàn " + idban + " không?", "Thoát", 2)) {
            String idHoaDon = tbInfo.getModel().getValueAt(0, 1).toString();
            try {
                HoaDonDB.update(idHoaDon);
                tbListTaleMouseClicked(evt);
            } catch (SQLException ex) {
                Logger.getLogger(jfrStaff.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnPayMouseClicked

