    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        int idHoaDon = (int) this.tbInfo.getModel().getValueAt(0, 1);
        String tenMon = (this.tbInfo.getModel().getValueAt(this.tbInfo.getSelectedRow(), 2).toString());
        String sql = "DELETE CT_HoaDon WHERE idHoaDon = ? AND idMenu = (SELECT id FROM dbo.Menu WHERE Ten = ?)";
        try {
            PreparedStatement ps = conn.prepareCall(sql);
            ps.setInt(1, idHoaDon);
            ps.setString(2, tenMon);
            ps.execute();

        } catch (SQLException ex) {
            Logger.getLogger(jfrStaff.class.getName()).log(Level.SEVERE, null, ex);
        }
        tbListTaleMouseClicked(evt);
    }//GEN-LAST:event_btnDeleteMouseClicked

