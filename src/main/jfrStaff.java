    private void tbListTaleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbListTaleMouseClicked
        try {
            String IDrow = (this.tbListTale.getModel().getValueAt(this.tbListTale.getSelectedRow(), 0)).toString();
            String sql1 = "SELECT * FROM Ban WHERE id='" + IDrow + "'";
            ResultSet rs = updateTable.showTextField(sql1);
            if (rs.next()) {
                idban = rs.getInt("id");
            }
            String sql2 = "SELECT Ban.id AS N'Bàn số', HoaDon.id AS N'Mã hóa đơn', Menu.Ten AS N'Tên món',\n"
                    + "SoLuong AS N'Số lượng', Gia N'Đơn giá', Gia*SoLuong AS N'Thành tiền'\n"
                    + "FROM Ban, HoaDon, CT_HoaDon, Menu\n"
                    + "WHERE Ban.id = '" + IDrow + "' AND Ban.id = HoaDon.idBan AND HoaDon.id = CT_HoaDon.idHoaDon\n"
                    + "AND CT_HoaDon.idMenu = Menu.id AND TrangThai = 0";
            updateTable.loadData(sql2, tbInfo);
            try {
                int rowCount = tbInfo.getModel().getRowCount();
                float totalPrice = 0;
                while (rowCount > 0) {
                    String priceInRow = (tbInfo.getModel().getValueAt(rowCount - 1, 5)).toString();
                    float f = Float.parseFloat(priceInRow);
                    totalPrice += f;
                    rowCount--;
                }
                txtTotalPrice.setText(String.valueOf(totalPrice));

            } catch (Exception e) {
            }
        } catch (Exception e) {
            JOptionPane.showInputDialog(null, e);
        }

    }//GEN-LAST:event_tbListTaleMouseClicked

